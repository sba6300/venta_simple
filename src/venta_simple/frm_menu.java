    private void recalculosx80segundos() {
        try {
            Timer timer = new Timer(80000, new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    double total = detalle.verVentas(jTable1);
                    jLabel2.setText(c_varios.formato_totales(total));
                    venta.resumenVentasMes(jTable2);
                    venta.resumenVentasAño(jTable3);
                }
            });
            timer.start();
            timer.setRepeats(true);
        } catch (Exception e) {
            System.out.println("Error grave " + e.getLocalizedMessage());
        }
    }

    private void validarCaja() {
        //comprobar que es primera vez para iniciar caja chica 
        caja.setFecha(c_varios.getFechaActual());
        boolean existe = caja.obtenerDatos();
        if (!existe) {
            jDialog1.setModal(true);
            jDialog1.setSize(400, 221);
            jDialog1.setLocationRelativeTo(null);
            jFormattedTextField1.setText(c_varios.formato_fecha_vista(c_varios.getFechaActual()));
            jTextField1.setText("");
            jTextField1.requestFocus();
            jDialog1.setVisible(true);
        }
    }


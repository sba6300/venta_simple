    private void llenar() {
        producto.setCodbarra(txt_codbarra.getText());
        producto.setNombre(txt_nombre.getText());
        producto.setCosto(Double.parseDouble(txt_costo.getText()));
        producto.setPrecio(Double.parseDouble(txt_precio.getText()));
    }

    private void limpiar() {
        txt_codbarra.setText("");
        txt_nombre.setText("");
        txt_costo.setText("");
        txt_precio.setText("");
        producto.setId(0);
    }


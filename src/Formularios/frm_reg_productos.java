    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        llenar();
        if (producto.getId() == 0) {
            producto.obtener_id();
            if (producto.insertar()) {
                JOptionPane.showMessageDialog(null, "Producto Agregado", "Productos", JOptionPane.INFORMATION_MESSAGE);
            }
            limpiar();
        } else {
            if (producto.actualizar()) {
                JOptionPane.showMessageDialog(null, "Producto Modificar", "Productos", JOptionPane.INFORMATION_MESSAGE);
                jButton2.doClick();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

